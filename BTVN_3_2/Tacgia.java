package BTVN_3_2;

public class Tacgia 
{
	private String name;
	private int namsinh;
	private int sosangtac;
	public Tacgia(String name, int namsinh, int sosangtac)
	{
		this.name = name;
		this.namsinh = namsinh;
		this.sosangtac = sosangtac;
	}
	public String getName() {
        return name;
    }

    public int namsinh() {
        return namsinh;
    }
    public int sosangtac() {
        return sosangtac;
    }

}
