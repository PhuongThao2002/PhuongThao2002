package Fruit;

public class Qua {
	private String name;
	private int gia, soluong;

	public Qua(String name, int gia, int soluong) {
		this.name = name;
		this.gia = gia;
		this.soluong = soluong;
	}

	public String getname() {
		return name;
	}

	public void setname(String name) {
		this.name = name;
	}

	public int getgia() {
		return gia;
	}

	public void setgia(int gia) {
		this.gia = gia;
	}

	public int getsoluong() {
		return soluong;
	}

	public void setsoluong(int soluong) {
		this.soluong = soluong;
	}
	
}
